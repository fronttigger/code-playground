function getSize(size: SizeType | number): { width: number; height: number } {
  if (typeof size === 'number') {
    return {
      width: size,
      height: size,
    }
  }

  switch (size) {
    case 'large': {
      return {
        width: 20,
        height: 20,
      }
    }
    default:
    case 'medium': {
      return {
        width: 16,
        height: 14,
      }
    }
    case 'small': {
      return {
        width: 11,
        height: 10,
      }
    }
  }
}

